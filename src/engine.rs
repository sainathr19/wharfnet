impl Engine for EvmEngine {
    fn name(&self) -> String {
        self.name.clone()
    }

    fn host_port(&self) -> u16 {
        self.host_port
    }

    fn compose_service(&self, mode: StateMode) -> String {
        // Ephemeral: load the baked tokens read-only, never dump. Persistent:
        // `--state` loads the session snapshot if present and dumps back to it
        // on exit and every interval, so a crash-restart resumes too.
        let state_args = match mode {
            StateMode::Ephemeral => {
                r#""--load-state", "/state/anvil-tokens.json""#.to_string()
            }
            StateMode::Persistent => format!(
                r#""--state", "/{session}", "--state-interval", "{interval}""#,
                session = self.session_rel_path(),
                interval = ANVIL_STATE_INTERVAL_SECS,
            ),
        };
        ANVIL_SERVICE_TEMPLATE
            .replace("{{NAME}}", &self.name)
            .replace("{{IMAGE}}", &self.image)
            .replace("{{PORT}}", &ANVIL_INTERNAL_PORT.to_string())
            .replace("{{CHAIN_ID}}", &self.chain_id.to_string())
            .replace("{{HOST_PORT}}", &self.host_port.to_string())
            .replace("{{STATE_ARGS}}", &state_args)
    }

    fn manifest_entry(&self) -> ChainEntry {
        ChainEntry {
            name: self.name.clone(),
            kind: "evm".to_string(),
            rpc: format!("http://127.0.0.1:{}", self.host_port),
            chain_id: self.chain_id,
            accounts: Self::accounts(),
            tokens: Self::tokens(),
        }
    }

    fn staged_files(&self, mode: StateMode) -> Vec<StagedFile> {
        match mode {
            // Refreshed on every boot so the chain always starts from the
            // known-good pre-deployed tokens.
            StateMode::Ephemeral => vec![StagedFile {
                rel_path: "state/anvil-tokens.json".to_string(),
                contents: ANVIL_TOKEN_STATE,
                if_absent: false,
            }],
            // Seed the session with the baked tokens only the first time; on
            // later boots the existing session (with the user's work) is kept.
            StateMode::Persistent => vec![StagedFile {
                rel_path: self.session_rel_path(),
                contents: ANVIL_TOKEN_STATE,
                if_absent: true,
            }],
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn anvil_constructor_sets_fields() {
        let e = EvmEngine::anvil("anvil-1", 8545, 31337);
        assert_eq!(e.name(), "anvil-1");
        assert_eq!(e.host_port(), 8545);
        assert_eq!(e.image, "ghcr.io/foundry-rs/foundry:stable");
        assert_eq!(e.chain_id, 31337);
    }

    #[test]
    fn compose_service_substitutes_every_placeholder() {
        for mode in [StateMode::Ephemeral, StateMode::Persistent] {
            let yaml = EvmEngine::anvil("anvil-2", 8546, 31338).compose_service(mode);
            assert!(yaml.contains("anvil-2:"));
            assert!(yaml.contains("ghcr.io/foundry-rs/foundry:stable"));
            assert!(yaml.contains("\"--chain-id\", \"31338\""));
            assert!(yaml.contains("\"8546:8545\""));
            assert!(!yaml.contains("{{"), "no placeholder should remain: {yaml}");
            assert!(!yaml.contains("}}"), "no placeholder should remain: {yaml}");
        }
    }

    #[test]
    fn ephemeral_compose_loads_baked_tokens_without_dumping() {
        let yaml = EvmEngine::anvil("anvil-1", 8545, 31337).compose_service(StateMode::Ephemeral);
        assert!(yaml.contains("\"--load-state\", \"/state/anvil-tokens.json\""));
        assert!(!yaml.contains("--state-interval"), "must not dump when ephemeral");
        assert!(!yaml.contains("--dump-state"), "must not dump when ephemeral");
        // The whole state dir is mounted so persistent runs can write dumps.
        assert!(yaml.contains("./state:/state"));
    }

    #[test]
    fn persistent_compose_loads_and_dumps_a_session_snapshot() {
        let yaml = EvmEngine::anvil("anvil-1", 8545, 31337).compose_service(StateMode::Persistent);
        assert!(yaml.contains("\"--state\", \"/state/session-anvil-1.json\""));
        assert!(yaml.contains("\"--state-interval\", \"5\""));
        // Uses --state (load+dump), not the ephemeral read-only load.
        assert!(!yaml.contains("--load-state"), "persistent uses --state, not --load-state");
    }

    #[test]
    fn manifest_entry_describes_the_chain() {
        let entry = EvmEngine::anvil("anvil-1", 8545, 31337).manifest_entry();
        assert_eq!(entry.kind, "evm");
        assert_eq!(entry.rpc, "http://127.0.0.1:8545");
        assert_eq!(entry.chain_id, 31337);
        assert_eq!(entry.accounts.len(), 3);
        assert_eq!(
            entry.accounts[0].address,
            "0xf39Fd6e51aad88F6F4ce6aB8827279cffFb92266"
        );
    }

    #[test]
    fn manifest_entry_lists_the_test_tokens() {
        let entry = EvmEngine::anvil("anvil-1", 8545, 31337).manifest_entry();
        let symbols: Vec<&str> = entry.tokens.iter().map(|t| t.symbol.as_str()).collect();
        assert_eq!(symbols, vec!["USDC", "WBTC"]);
        let usdc = &entry.tokens[0];
        assert_eq!(usdc.address, "0x5FbDB2315678afecb367f032d93F642f64180aa3");
        assert_eq!(usdc.decimals, 6);
        assert_eq!(entry.tokens[1].decimals, 8);
    }

    #[test]
    fn ephemeral_staged_files_refresh_the_baked_snapshot() {
        let files = EvmEngine::anvil("anvil-1", 8545, 31337).staged_files(StateMode::Ephemeral);
        assert_eq!(files.len(), 1);
        assert_eq!(files[0].rel_path, "state/anvil-tokens.json");
        assert!(!files[0].if_absent, "baked snapshot is always refreshed");
        // The snapshot really contains the deployed USDC contract.
        assert!(
            files[0]
                .contents
                .contains("5fbdb2315678afecb367f032d93f642f64180aa3")
        );
    }

    #[test]
    fn persistent_staged_files_seed_a_per_chain_session_only_if_absent() {
        let files = EvmEngine::anvil("anvil-2", 8546, 31338).staged_files(StateMode::Persistent);
        assert_eq!(files.len(), 1);
        assert_eq!(files[0].rel_path, "state/session-anvil-2.json");
        assert!(files[0].if_absent, "must never clobber a saved session");
        assert!(
            files[0]
                .contents
                .contains("5fbdb2315678afecb367f032d93f642f64180aa3")
        );
    }
}
