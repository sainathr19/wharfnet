//! Chain engines. Each engine knows how to (a) describe itself as a
//! docker-compose service and (b) describe how to reach it in the manifest.
//!
//! wharfnet does not implement chains — it wraps best-in-class engines:
//! Anvil for EVM (here), with Solana (solana-test-validator/Surfpool) and
//! Starknet (starknet-devnet-rs) landing in M2 as additional `Engine` impls.
//!
//! Compose service YAML lives in `src/resources/docker/services/` and is
//! embedded into the binary at compile time — edit those templates, not Rust
//! strings.

use crate::manifest::{Account, ChainEntry, Token};

/// Internal port the engine listens on inside its container.
const ANVIL_INTERNAL_PORT: u16 = 8545;

/// How often Anvil flushes its state to disk in persistent mode. A small
/// interval keeps the on-disk session close to live so little is lost if the
/// process is killed rather than shut down gracefully.
const ANVIL_STATE_INTERVAL_SECS: u16 = 5;

/// Compose service template for an Anvil-backed EVM chain.
const ANVIL_SERVICE_TEMPLATE: &str = include_str!("resources/docker/services/anvil.yml");

/// Pre-baked Anvil state snapshot with the test tokens deployed and seeded.
/// Generated by `scripts/gen-token-state.sh`. In ephemeral mode it is loaded
/// read-only via `--load-state`; in persistent mode a copy of it seeds the
/// chain's session snapshot the first time. Mounted under `./state/`.
const ANVIL_TOKEN_STATE: &str = include_str!("resources/state/anvil-tokens.json");

/// Whether a chain boots fresh every time or resumes (and keeps saving) its
/// runtime state across restarts.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum StateMode {
    /// Load the baked snapshot and never dump — deterministic, disposable.
    /// Every boot starts from the same pre-deployed tokens and seeded accounts.
    Ephemeral,
    /// Load from (and continuously dump to) a per-chain session snapshot, so
    /// balances, transactions, and deployments survive `down` → `up --resume`.
    Persistent,
}

/// A file an engine needs written under the state dir before its container
/// boots (e.g. a chain snapshot mounted into the container).
pub struct StagedFile {
    /// Path relative to the state dir; matches the compose volume source.
    pub rel_path: String,
    pub contents: &'static str,
    /// When true, write only if the destination does not already exist. Used to
    /// seed a session snapshot without ever clobbering saved state.
    pub if_absent: bool,
}

pub trait Engine {
    /// Service / container name, e.g. "anvil-1".
    fn name(&self) -> String;
    /// The host port the RPC is published on (used for health checks).
    fn host_port(&self) -> u16;
    /// A docker-compose service fragment, indented two spaces under `services:`.
    fn compose_service(&self, mode: StateMode) -> String;
    /// How to reach this chain, for the manifest.
    fn manifest_entry(&self) -> ChainEntry;
    /// Files to write under the state dir before boot. Defaults to none.
    fn staged_files(&self, _mode: StateMode) -> Vec<StagedFile> {
        Vec::new()
    }
}

